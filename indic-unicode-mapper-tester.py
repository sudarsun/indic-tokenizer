#!/usr/bin/env python3

from indic_unicode_mapper import IndicUnicodeMapper

# create the mapper object
m = IndicUnicodeMapper()

# create the encoded and decoded texts
t = "கட்டடக்கலை என்பது கட்டடங்கள் மற்றும் அதன் உடல் கட்டமைப்புகளை வடிவமைத்தல், செயல்முறைத் திட்டமிடல், மற்றும் கட்டடங்கள் கட்டுவதை உள்ளடக்கியதாகும். ടാറ്റയുടെ ഇംപാക്ട് 2.0 ഡിസൈന് ഭാഷ്യം പിന്തുടരുന്ന ആദ്യത്തെ കാറാണ് ഹാരിയര് "
